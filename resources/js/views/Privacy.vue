            öffentlichen Interesse liegt oder in Ausübung öffentlicher Gewalt erfolgt, die dem Verantwortlichen übertragen
            wurde;
        </p>
        <p>
            (3) aus Gründen des öffentlichen Interesses im Bereich der öffentlichen Gesundheit gemäß Art.9 Abs.2
        </p>
        <p>
            lit.h und i sowie Art.9 Abs.3 DSGVO; (4) für im öffentlichen Interesse liegende Archivzwecke, wissenschaftliche oder
            historische
        </p>
        <p>
            Forschungszwecke oder für statistische Zwecke gem. Art.89 Abs.1 DSGVO, soweit das unter Abschnitt a) genannte Recht
            voraussichtlich die Verwirklichung der Ziele dieser Verarbeitung unmöglich macht oder ernsthaft beeinträchtigt, oder
        </p>
        <p>
            (5) zur Geltendmachung, Ausübung oder Verteidigung von Rechtsansprüchen.
        </p>
        <br>
        <p>
            <strong>5. Recht auf Unterrichtung</strong>
        </p>
        <p>
            Haben Sie das Recht auf Berichtigung, Löschung oder Einschränkung der Verarbeitung gegenüber dem Verantwortlichen
            geltend gemacht, ist dieser verpflichtet, allen Empfängern, denen die Sie betreffenden personenbezogenen Daten
            offengelegt wurden, diese Berichtigung oder Löschung der Daten oder Einschränkung der Verarbeitung mitzuteilen, es
            sei denn, dies erweist sich als unmöglich oder ist mit einem unverhältnismäßigen Aufwand verbunden. Ihnen steht
            gegenüber dem Verantwortlichen das Recht zu, über diese Empfänger unterrichtet zu werden.
        </p>
        <br>
        <p>
            <strong>6. Recht auf Datenübertragbarkeit </strong>
        </p>
        <p>
            Sie haben das Recht, die Sie betreffenden personenbezogenen Daten, die Sie dem Verantwortlichen bereitgestellt
            haben, in einem strukturierten, gängigen und maschinenlesbaren Format zu erhalten. Außerdem haben Sie das Recht
            diese Daten einem anderen Verantwortlichen ohne Behinderung durch den Verantwortlichen, dem die personenbezogenen
            Daten bereitgestellt wurden, zu übermitteln, sofern
        </p>
        <p>
            (1) die Verarbeitung auf einer Einwilligung gem. Art.6 Abs.1 lit.a DSGVO oder Art.9 Abs.2 lit.a DSGVO oder auf einem
            Vertrag gem. Art.6 Abs.1 lit.b DSGVO beruht und
        </p>
        <p>
            (2) die Verarbeitung mithilfe automatisierter Verfahren erfolgt.
        </p>
        <p>
            In Ausübung dieses Rechts haben Sie ferner das Recht, zu erwirken, dass die Sie betreffenden personenbezogenen Daten
            direkt von einem Verantwortlichen einem anderen Verantwortlichen übermittelt werden, soweit dies technisch machbar
            ist. Freiheiten und Rechte anderer Personen dürfen hierdurch nicht beeinträchtigt werden. Das Recht auf
            Datenübertragbarkeit gilt nicht für eine Verarbeitung personenbezogener Daten, die für die Wahrnehmung einer Aufgabe
            erforderlich ist, die im öffentlichen Interesse liegt oder in Ausübung öffentlicher Gewalt erfolgt, die dem
            Verantwortlichen übertragen wurde.
        </p>
        <br>
        <p>
            <strong>7. Widerspruchsrecht </strong>
        </p>
