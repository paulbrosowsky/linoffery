            beschränkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecken unmöglich macht
            oder ernsthaft beeinträchtigt und die Beschränkung für        
            die Erfüllung der Forschungs- oder Statistikzwecke notwendig ist.
        </p>
        <br>
        <p>
            <strong>8. Recht auf Widerruf der datenschutzrechtlichen Einwilligungserklärung </strong>
        </p>
        <p>
            Sie haben das Recht, Ihre datenschutzrechtliche Einwilligungserklärung jederzeit zu widerrufen. Durch den Widerruf
            der Einwilligung wird die Rechtmässigkeit, der aufgrund der Einwilligung bis zum Widerruf erfolgten Verarbeitung
