            (8) das Bestehen einer automatisierten Entscheidungsfindung einschließlich Profiling gemäß Art.22 Abs.1 und 4 DSGVO
            und – zumindest in diesen Fällen – aussagekräftige Informationen über die involvierte Logik sowie die Tragweite und
            die angestrebten Auswirkungen einer derartigen Verarbeitung für die betroffene Person.
        </p>
        <p>
            Ihnen steht das Recht zu, Auskunft darüber zu verlangen, ob die Sie betreffenden personenbezogenen Daten in ein
            Drittland oder an eine internationale Organisation übermittelt werden. In diesem Zusammenhang können Sie verlangen,
            über die geeigneten Garantien gem. Art.46 DSGVO im Zusammenhang mit der Übermittlung unterrichtet zu werden. Dieses
            Auskunftsrecht kann insoweit beschränkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder
            Statistikzwecke unmöglich macht oder ernsthaft beeinträchtigt und die Beschränkung für die Erfüllung der Forschungs-
            oder Statistikzwecke notwendig ist.
        </p>
        <br>
        <p>
            <strong>2. Recht auf Berichtigung </strong>
        </p>
        <p>
            Sie haben ein Recht auf Berichtigung und/oder Vervollständigung gegenüber dem Verantwortlichen, sofern die
            verarbeiteten personenbezogenen Daten, die Sie betreffen, unrichtig oder unvollständig sind. Der Verantwortliche hat
            die Berichtigung unverzüglich vorzunehmen. Ihr Recht auf Berichtigung kann insoweit beschränkt werden, als es
            voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke unmöglich macht oder ernsthaft
            beeinträchtigt und die Beschränkung für
            die Erfüllung der Forschungs- oder Statistikzwecke notwendig ist.
        </p>
        <br>
        <p>
            <strong>3. Recht auf Einschränkung der Verarbeitung </strong>
        </p>
        <p>
            Unter den folgenden Voraussetzungen können Sie die Einschränkung der Verarbeitung der Sie betreffenden
            personenbezogenen Daten verlangen:
        </p>
        <p>
            (1) wenn Sie die Richtigkeit, der Sie betreffenden personenbezogenen für eine Dauer bestreiten, die es dem
            Verantwortlichen ermöglicht, die Richtigkeit der personenbezogenen Daten zu überprüfen; (2) die Verarbeitung
            unrechtmäßig ist und Sie die Löschung der personenbezogenen Daten ablehnen und stattdessen die Einschränkung der
            Nutzung der personenbezogenen Daten verlangen; (3) der Verantwortliche die personenbezogenen Daten für die Zwecke
            der Verarbeitung nicht länger benötigt, Sie diese jedoch zur Geltendmachung, Ausübung oder Verteidigung von
            Rechtsansprüchen benötigen, oder (4) wenn Sie Widerspruch gegen die Verarbeitung gemäß Art.21 Abs.1 DSGVO eingelegt
            haben und noch nicht feststeht, ob die berechtigten Gründe des Verantwortlichen gegenüber Ihren Gründen überwiegen.
            Wurde die Verarbeitung der Sie betreffenden personenbezogenen Daten eingeschränkt, dürfen diese Daten – von ihrer
            Speicherung abgesehen – nur mit Ihrer Einwilligung oder zur Geltendmachung, Ausübung oder Verteidigung von
            Rechtsansprüchen oder zum Schutz der Rechte einer anderen
        </p>
        <p>
            natürlichen oder juristischen Person oder aus Gründen eines wichtigen öffentlichen Interesses der Union oder eines
            Mitgliedstaats verarbeitet werden. Wurde die Einschränkung der Verarbeitung nach den o.g. Voraussetzungen
            eingeschränkt, werden Sie von dem Verantwortlichen unterrichtet bevor die Einschränkung aufgehoben wird. Ihr Recht
            auf Einschränkung der Verarbeitung kann insoweit beschränkt werden, als es voraussichtlich die Verwirklichung der
            Forschungs- oder Statistikzwecke unmöglich macht oder ernsthaft beeinträchtigt und die Beschränkung für die
            Erfüllung der Forschungs- oder Statistikzwecke notwendig ist.
        </p>
        <br>
        <p>
            <strong>4. Recht auf Löschung </strong>
        </p>
        <p>
            a) Löschungspflicht
        </p>
        <p>
            Sie können von dem Verantwortlichen verlangen, dass die Sie betreffenden personenbezogenen Daten unverzüglich
            gelöscht werden, und der Verantwortliche ist verpflichtet, diese Daten unverzüglich zu löschen, sofern einer der
            folgenden Gründe zutrifft:
        </p>
        <p>
            (1) Die Sie betreffenden personenbezogenen Daten sind für die Zwecke, für die sie erhoben oder auf sonstige Weise
            verarbeitet wurden, nicht mehr notwendig.
        </p>
        <p>
            (2) Sie widerrufen Ihre Einwilligung, auf die sich die Verarbeitung gem. Art.6 Abs.1 lit.a oder Art.9 Abs.2 lit.a
            DSGVO stützte, und es fehlt an einer anderweitigen Rechtsgrundlage für die Verarbeitung.
        </p>
        <p>
            (3) Sie legen gem. Art.21 Abs.1 DSGVO Widerspruch gegen die Verarbeitung ein und es liegen keine
        </p>
        <p>
            vorrangigen berechtigten Gründe für die Verarbeitung vor, oder Sie legen gem. Art.21 Abs.2 DSGVO Widerspruch gegen
            die Verarbeitung ein.
        </p>
        <p>
            (4) Die Sie betreffenden personenbezogenen Daten wurden unrechtmäßig verarbeitet.
        </p>
        <p>
            (5) Die Löschung der Sie betreffenden personenbezogenen Daten ist zur Erfüllung einer rechtlichen Verpflichtung nach
            dem Unionsrecht oder dem Recht der Mitgliedstaaten erforderlich, dem der Verantwortliche unterliegt.
        </p>
        <p>
            (6) Die Sie betreffenden personenbezogenen Daten wurden in Bezug auf angebotene Dienste der Informationsgesellschaft
            gemäß Art.8 Abs.1 DSGVO erhoben.
        </p>
        <br>
        <p>
            <strong>b) Information an Dritte </strong>
        </p>
        <p>
            Hat der Verantwortliche die Sie betreffenden personenbezogenen Daten öffentlich gemacht und ist er gem. Art.17 Abs.1
            DSGVO zu deren Löschung verpflichtet, so trifft er unter Berücksichtigung der verfügbaren Technologie und der
            Implementierungskosten angemessene Maßnahmen, auch technischer Art, um für die Datenverarbeitung Verantwortliche,
            die die personenbezogenen Daten verarbeiten, darüber zu informieren, dass Sie als betroffene Person von ihnen die
            Löschung aller Links zu diesen personenbezogenen Daten oder von Kopien oder Replikationen dieser personenbezogenen
            Daten verlangt haben.
        </p>
        <p>
            <strong>c) Ausnahmen </strong>
        </p>
        <p>
            Das Recht auf Löschung besteht nicht, soweit die Verarbeitung erforderlich ist
        </p>
        <p>
            (1) zur Ausübung des Rechts auf freie Meinungsäußerung und Information;
        </p>
        <p>
            (2) zur Erfüllung einer rechtlichen Verpflichtung, die die Verarbeitung nach dem Recht der Union oder der
