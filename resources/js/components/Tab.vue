<template>
    <div v-if="isActive">
        <slot></slot>
    </div>
</template>
<script>
    export default {
        props:{
            name:{ required: true },
            selected:{ default: false }
        },  
        
        data(){
            return{
                isActive: this.selected,
            }
        },

        computed:{
            href(){
                return '#' + this.name.toLowerCase().replace(/ /g, '-');
              
            }
        }
    }
</script>
